package mx.redpoint.isa.bean;

public class Finanzas {
	private String alias;
	private String tipo;
	private String montoinicial;
	private String numerocuenta;
	private String numerotarjeta;
	private String numeroclabe;
	private String cuotamensual;
	public String getAlias() {
		return alias;
	}
	public void setAlias(String alias) {
		this.alias = alias;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getMontoinicial() {
		return montoinicial;
	}
	public void setMontoinicial(String montoinicial) {
		this.montoinicial = montoinicial;
	}
	public String getNumerocuenta() {
		return numerocuenta;
	}
	public void setNumerocuenta(String numerocuenta) {
		this.numerocuenta = numerocuenta;
	}
	public String getNumerotarjeta() {
		return numerotarjeta;
	}
	public void setNumerotarjeta(String numerotarjeta) {
		this.numerotarjeta = numerotarjeta;
	}
	public String getNumeroclabe() {
		return numeroclabe;
	}
	public void setNumeroclabe(String numeroclabe) {
		this.numeroclabe = numeroclabe;
	}
	public String getCuotamensual() {
		return cuotamensual;
	}
	public void setCuotamensual(String cuotamensual) {
		this.cuotamensual = cuotamensual;
	}
	
}
